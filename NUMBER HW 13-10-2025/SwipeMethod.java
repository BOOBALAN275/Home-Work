class SwipeMethod
{
	public static void main(String[] args)
	{
		int x = 50;
		int y = 100;
		int temp;
		
		temp = 50;
		x = y;
		y = temp;
		
		System.out.println("Already Asign a Value is x = 50, But I can Swipe a Number is - "+x);
		System.out.println("Already Asign a Value is y = 100, But I can Swipe a Number is - "+y);
		
		
	}
}