class OverAllOperators
{
	public static void main(String[] args)
		{
			int Raj=5;
			int Ravi=10;
			int Kumar=Raj+Ravi;
			
			System.out.println(Kumar);
			
			Raj=Raj+30;
			
			System.out.println(Raj);
			
			boolean Kannan=Kumar>Ravi;
			
			System.out.println(Kannan);
			
			boolean Kavi=(Ravi<Raj)&&(Kumar>Raj);
			
			System.out.println(Kavi);
		}
}